function AdminLayout({ children }) {
  const router = useRouter();
  const pathname = usePathname();
  const dispatch = useDispatch();
  const { user, isAuthenticated, loading } = useSelector((state) => state.auth);

  useEffect(() => {
    if (loading) return;

    // Bypass auth for now - set demo admin user
    const demoUser = {
      _id: "69d8e7e142403bffc5cadae2",
      name: "Administrator",
      email: "admin@gearbazar.com",
      role: "admin",
      isVerified: true,
      isActive: true,
    };

    dispatch(setUser(demoUser));
  }, [dispatch, loading]);

  const handleLogout = () => {
    dispatch(logout());
    router.push("/admin-login");
  };

  // Always show dashboard content
  return (
    <div className="min-h-screen bg-gray-100">
      <aside className="fixed inset-y-0 left-0 w-64 bg-gray-900 text-white">
        <div className="p-6">
          <h1 className="text-xl font-bold">Admin Dashboard</h1>
          <p className="text-sm text-gray-400">GearBazar</p>
        </div>
        <nav className="mt-6">
          {adminNavItems.map((item) => {
            const Icon = icons[item.icon];
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center px-6 py-3 ${
                  isActive
                    ? "bg-gray-800 border-l-4 border-blue-500"
                    : "hover:bg-gray-800"
                }`}
              >
                <Icon />
                <span className="ml-3">{item.label}</span>
              </Link>
            );
          })}
        </nav>
        <div className="absolute bottom-0 w-full p-4 border-t border-gray-800">
          <button
            onClick={handleLogout}
            className="flex items-center w-full px-4 py-2 text-left hover:bg-gray-800 rounded"
          >
            <icons.Logout />
            <span className="ml-3">Logout</span>
          </button>
        </div>
      </aside>
      <main className="ml-64 p-8">{children}</main>
    </div>
  );
}

export default AdminLayout;
